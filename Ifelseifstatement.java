 public class NumberCheckExample {
    public static void main(String[] args) {
        int number = 300;
        if (number > 400) {
            System.out.println("The number is greater than 400");
        } else if (number == 300) {
            System.out.println("The number is equal to 300");
        } else {
            System.out.println("The number is neither greater than 400 nor equal to 300");
        }
    }
}
